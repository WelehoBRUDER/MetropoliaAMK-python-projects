number_1 = int(input("Enter the first number: "))
number_2 = int(input("Enter the second number: "))
number_3 = int(input("Enter the third number: "))

# _ to distinguish from built-in functions
sum_ = number_1 + number_2 + number_3
multiplication = number_1 * number_2 * number_3
average = (number_1 + number_2 + number_3) / 3
print(f"The sum of the numbers is: {sum_}")
print(f"The multiplication of the numbers is: {multiplication}")
print(f"The average of the numbers is: {average}")
